#include "edge.hpp"

Edge::Edge():weight_(0),color_(0),lineType_(straight),arrowheads_(true)
{
}

void Edge::setWeight( int weight )
{
  weight_ = weight;
}

int Edge::getWeight( )
{
  return weight_;
}

void Edge::setColor( uint32_t color )
{
  color_ = color;
}

uint32_t Edge::getColor( )
{
  return color_;
}

void Edge::setLineType( LineType lineType )
{
  lineType_ = lineType;
}

LineType Edge::getLineType( )
{
  return lineType_;
}

void Edge::setArrows( bool on )
{
  arrowheads_ = on;
}

bool Edge::getArrows( )
{
  return arrowheads_;
}
